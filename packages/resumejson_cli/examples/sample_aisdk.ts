async function main(): Promise<void> {
	const { text } = await generateText({
		model: openai("gpt-4.1-nano"),
		prompt: 'Write a one-sentence summary of what a resume is.',
	});

	console.log(text);
}

///////////////////////////////////////////////////////////////////////////////
